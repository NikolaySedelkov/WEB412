/** 
 * для решения подобных задач, где количество повторений четко указывается, можно использовать специальный цикл for
 * который позволяет создать локально внутри себя дополнительные переменные - одно из этих переменных можно сделать счётчик цикла
 * 
 * Паттерн:
 * 
 * for(
 *      let COUNTER = START_VALUE;
 *      COUNTER < END_VALUE;
 *      COUNTER += STEP
 * ) {
 *  // Тело цикла
 * }
*/

