// Максимум из трех чисел
{
    const a = +prompt("Первое число");
    const b = +prompt("Второе число");
    const c = +prompt("Третье число");

    let max;
    // Ищу максимальное значение среди первый двух чисел - Максимальное значение сохраню, как текущее максимальное
    if(a > b) {
        max = a;
    } else {
        max = b;
    }

    //  Масимальное значение проверяем с оставщимся числом
    if(c > max) {
        max = c;
    }

    alert("Максимум: " + max);
}


