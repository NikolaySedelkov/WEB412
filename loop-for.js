{
    let N = +prompt('Укажите, кол-во чисел, которых хотите ввести');

    let max = Number.MIN_SAFE_INTEGER;
    for(let i = 0; i < N; i += 1) {
        let x = +prompt('Введите число[' + (i+1) +']');
        if(x > max) {
            max = x;
        }
    }
}

//  посчитать среднее значение чисел введенных пользоваталем, которые 3х значные
{
    let N = +prompt('Укажите, кол-во чисел, которых хотите ввести');

    let sum = 0, count = 0;
    for(let i = 0; i < N; i += 1) {
        let x = +prompt('Введите число[' + (i+1) +']');
        if(100 <= x && x < 1000) {
            sum += x;
            count += 1;
        }
    }

    if(count !== 0)
        alert('Среднее значение подходящих цифр = ' + (sum / count));
    else 
    alert('Таких чисел нет')
}

