// ход слона
{
    const x1 = +prompt("Фигура 1\nНомер строки");
    const y1 = +prompt("Фигура 1\nНомер стобца");

    const x2 = +prompt("Фигура 2\nНомер строки");
    const y2 = +prompt("Фигура 2\nНомер стобца");

    // Растояние между строка и столбцами
    let dx = x2 - x1;
    let dy = y2 - y1;

    // Расчёт модуля для растояними по X
    if(dx < 0) {
        dx = -dx;
    }

    // Расчёт модуля для растояними по Y
    if(dy < 0) {
        dy = -dy;
    }

    // Если растояния по разным координатам равны
    if(dx === dy) {
        alert("YES");
    } else {
        alert("NO");
    }
}

// Модуль числа
{
    let x = +prompt("Введите число");

    // Если число отрицательное
    if(x < 0) {
        // То добавляем к числу второй минус, чтобы минус на минус дал плюс
        x = -x;
    }

    alert("Модуль: " + x);
}

